public class Fisica extends Cliente{
    private String cpf;
    
    public Fisica(String n, String e, String t, String cpf){
        super(n,e,t);
        this.cpf = cpf;
    }
    public String getCPF(){
        return this.cpf;
    }
    public void imprimir(){
        super.imprimir();
        System.out.println("CPF: " + cpf);
    }
}
